	public ConfigReader(String aConfigPath) {
		initF();
		initG();
		iConfNames = new Vector<String>();
		iConfValues = new SmartStringMap();
		fillNames();
		try {
			BufferedReader configReader = new BufferedReader(new FileReader(aConfigPath));
			while (configReader.ready()) {
				String line = configReader.readLine();
				if (line.length() > 0) {
					// sanity check, no space at the end of line
					if (line.lastIndexOf(" ") == line.length() - 1) {
						System.err.println("Warning: space found in config at the end of line: '" + line + "'. You should have no lines ending in space in the configuration!");
					}
					
					String[] params = line.split(": ");				
					if (params.length == 2) {
						for (String s : iConfNames) {
							if (params[0].equals(s)) {
								iConfValues.put(s, params[1]);
							}
						}
					}
				}
			}
			configReader.close();
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
		if (getIsDebugEnabled())
			print();
	}
